function MorInfoFerst() {
    var ferstbtn=document.getElementById("ferst-btn");
    var ferst=document.getElementById("ferst");
    if (ferst.style.display === "none") {
        ferst.style.display ="inline";
    } else {
        ferst.style.display ="none";
    }
}

